from io import BytesIO

from reportlab.lib import colors
from reportlab.lib.colors import HexColor
from reportlab.lib.enums import TA_CENTER, TA_LEFT
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib.units import inch
from reportlab.lib.pagesizes import letter

from reportlab.platypus import (
    SimpleDocTemplate,
    Paragraph,
    Spacer,
    Table,
    TableStyle,
    PageBreak,
    ListFlowable,
    ListItem,
    KeepTogether,
)


class PDFReportGenerator:
    """
    Generates a professional NetInsight Infrastructure Report.
    """

    def __init__(self, domain, report, generated_at):

        self.domain = domain
        self.report = report
        self.generated_at = generated_at

        styles = getSampleStyleSheet()

        # -------------------------------------------------
        # Color Palette
        # -------------------------------------------------

        self.primary = HexColor("#2563EB")
        self.success = HexColor("#16A34A")
        self.warning = HexColor("#F59E0B")
        self.danger = HexColor("#DC2626")
        self.dark = HexColor("#1E293B")
        self.light = HexColor("#F8FAFC")

        # -------------------------------------------------
        # Styles
        # -------------------------------------------------

        self.title_style = ParagraphStyle(
            "Title",
            parent=styles["Title"],
            fontName="Helvetica-Bold",
            fontSize=28,
            alignment=TA_CENTER,
            textColor=self.primary,
            spaceAfter=10,
        )

        self.heading_style = ParagraphStyle(
            "Heading",
            parent=styles["Heading1"],
            fontName="Helvetica-Bold",
            fontSize=18,
            textColor=self.dark,
            spaceBefore=18,
            spaceAfter=10,
        )

        self.subheading_style = ParagraphStyle(
            "SubHeading",
            parent=styles["Heading2"],
            fontName="Helvetica-Bold",
            fontSize=13,
            textColor=self.primary,
            spaceBefore=10,
            spaceAfter=6,
        )

        self.body_style = ParagraphStyle(
            "Body",
            parent=styles["BodyText"],
            fontName="Helvetica",
            fontSize=10,
            leading=18,
            textColor=colors.black,
        )

        self.small_style = ParagraphStyle(
            "Small",
            parent=styles["BodyText"],
            fontSize=8,
            leading=12,
            textColor=colors.grey,
        )

        self.center_style = ParagraphStyle(
            "Center",
            parent=self.body_style,
            alignment=TA_CENTER,
        )

    # =====================================================
    # Footer
    # =====================================================

    def _footer(self, canvas, doc):

        canvas.saveState()

        canvas.setStrokeColor(colors.lightgrey)

        canvas.line(
            doc.leftMargin,
            0.72 * inch,
            doc.width + doc.leftMargin,
            0.72 * inch,
        )

        canvas.setFont("Helvetica", 9)

        canvas.setFillColor(colors.grey)

        canvas.drawString(
            doc.leftMargin,
            0.50 * inch,
            "Generated by NetInsight"
        )

        canvas.drawRightString(
            doc.leftMargin + doc.width,
            0.50 * inch,
            f"Page {doc.page}"
        )

        canvas.restoreState()

    # =====================================================
    # Helper
    # =====================================================

    def _section_title(self, title):

        return Paragraph(
            title,
            self.heading_style
        )

    # =====================================================
    # Helper
    # Creates a clean two-column information table
    # =====================================================

    def _info_table(self, rows):

        formatted_rows = []

        for row in rows:

            formatted_row = []

            for cell in row:

                if isinstance(cell, list):
                    cell = ", ".join(str(x) for x in cell)

                elif cell is None:
                    cell = "-"

                else:
                    cell = str(cell)

                formatted_row.append(
                    Paragraph(
                        cell,
                        self.body_style
                    )
                )

            formatted_rows.append(formatted_row)

        table = Table(
            formatted_rows,
            colWidths=[2.1 * inch, 4.2 * inch]
        )

        table.setStyle(

            TableStyle(

                [

                    ("BACKGROUND", (0, 0), (0, -1), self.light),

                    ("GRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),

                    ("BOTTOMPADDING", (0, 0), (-1, -1), 8),

                    ("TOPPADDING", (0, 0), (-1, -1), 8),

                    ("VALIGN", (0, 0), (-1, -1), "TOP"),

                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),

                    ("TEXTCOLOR", (0, 0), (0, -1), self.dark),

                ]

            )

        )

        return table

    # =====================================================
    # Helper
    # Creates a colored status badge
    # =====================================================

    def _status_badge(self, text, color):

        badge = Table(
            [[Paragraph(
                f"<font color='white'><b>{text}</b></font>",
                self.center_style
            )]],
            colWidths=1.2 * inch
        )

        badge.setStyle(

            TableStyle(

                [

                    ("BACKGROUND", (0, 0), (-1, -1), color),

                    ("ALIGN", (0, 0), (-1, -1), "CENTER"),

                    ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),

                    ("BOX", (0, 0), (-1, -1), 0, color),

                    ("BOTTOMPADDING", (0, 0), (-1, -1), 6),

                    ("TOPPADDING", (0, 0), (-1, -1), 6),

                ]

            )

        )

        return badge

    # =====================================================
    # Helper
    # Adds bullet lists
    # =====================================================

    def _bullet_list(self, items):

        bullets = []

        for item in items:

            bullets.append(

                ListItem(

                    Paragraph(
                        str(item),
                        self.body_style
                    )

                )

            )

        return ListFlowable(
            bullets,
            bulletType="bullet"
        )

    # =====================================================
    # Main PDF Generator
    # =====================================================
    
    def generate(self):
        """
        Generate the complete PDF report.
        Returns:
            BytesIO: PDF file buffer
        """
    
        buffer = BytesIO()
    
        doc = SimpleDocTemplate(
            buffer,
            pagesize=letter,
            rightMargin=0.5 * inch,
            leftMargin=0.5 * inch,
            topMargin=0.6 * inch,
            bottomMargin=0.8 * inch,
            title=f"NetInsight Infrastructure Report - {self.domain}",
            author="NetInsight",
        )
    
        story = []
    
        # -------------------------------------------------
        # Cover Page
        # -------------------------------------------------
    
        self._add_cover_page(story)
    
        # -------------------------------------------------
        # Executive Summary
        # -------------------------------------------------
    
        self._add_executive_summary(story)
    
        # -------------------------------------------------
        # Health Breakdown
        # -------------------------------------------------
    
        self._add_health_breakdown(story)
    
        # -------------------------------------------------
        # Infrastructure Sections
        # -------------------------------------------------
    
        self._add_dns_section(story)
        self._add_whois_section(story)
        self._add_ip_hosting_section(story)
        self._add_http_section(story)
        self._add_ssl_section(story)
        self._add_security_headers_section(story)
        self._add_email_security_section(story)
        self._add_cdn_section(story)
        self._add_technology_section(story)
        self._add_recommendations_section(story)
    
        # -------------------------------------------------
        # Build PDF
        # -------------------------------------------------
    
        doc.build(
            story,
            onFirstPage=self._footer,
            onLaterPages=self._footer,
        )
    
        buffer.seek(0)
    
        return buffer
    
    # =====================================================
    # Cover Page
    # =====================================================
    
    def _add_cover_page(self, story):
        """
        Adds the report cover page.
        """
    
        story.append(Spacer(1, 0.6 * inch))
    
        # -------------------------------------------------
        # Report Title
        # -------------------------------------------------
    
        story.append(
            Paragraph(
                "NetInsight",
                self.title_style
            )
        )
    
        story.append(
            Paragraph(
                "Website Infrastructure Analysis Report",
                self.subheading_style
            )
        )
    
        story.append(Spacer(1, 0.35 * inch))
    
        # -------------------------------------------------
        # Domain Information
        # -------------------------------------------------
    
        cover_table = self._info_table([
            ["Target Domain", self.domain],
            ["Generated On", self.generated_at],
            ["Report Type", "Infrastructure & Security Assessment"],
            ["Generated By", "NetInsight"],
        ])
    
        story.append(cover_table)
    
        story.append(Spacer(1, 0.45 * inch))
    
        # -------------------------------------------------
        # Health Score
        # -------------------------------------------------
    
        health = (
            self.report.get("health", {})
                .get("data", {})
        )
    
        overall_score = health.get("score", 0)
    
        if overall_score >= 90:
            color = self.success
            status = "Excellent"
    
        elif overall_score >= 75:
            color = self.primary
            status = "Good"
    
        elif overall_score >= 60:
            color = self.warning
            status = "Fair"
    
        else:
            color = self.danger
            status = "Needs Attention"
    
        score_table = Table(
            [
                [
                    Paragraph(
                        f"""
                        <para align="center">
                        <font size="32" color="{color}">
                        <b>{overall_score}</b>
                        </font><br/>
                        <font size="12"><b>{status}</b></font>
                        </para>
                        """,
                        self.center_style,
                    )
                ]
            ],
            colWidths=[2.3 * inch],
        )
    
        score_table.setStyle(
            TableStyle([
                ("BACKGROUND", (0, 0), (-1, -1), self.light),
                ("BOX", (0, 0), (-1, -1), 1, color),
                ("GRID", (0, 0), (-1, -1), 0.5, colors.lightgrey),
                ("TOPPADDING", (0, 0), (-1, -1), 18),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 18),
                ("ALIGN", (0, 0), (-1, -1), "CENTER"),
            ])
        )
    
        story.append(score_table)
    
        story.append(Spacer(1, 0.45 * inch))
    
        # -------------------------------------------------
        # Report Description
        # -------------------------------------------------
    
        description = """
        This report provides a comprehensive analysis of the target website's
        infrastructure including DNS configuration, SSL/TLS certificate,
        HTTP response, hosting information, email security, technology stack,
        security headers, CDN detection, and overall security posture.
    
        The recommendations provided throughout this report are intended to
        improve the website's security, reliability, and performance.
        """
    
        story.append(
            Paragraph(
                description,
                self.body_style
            )
        )
    
        story.append(Spacer(1, 0.4 * inch))
    
        # -------------------------------------------------
        # Disclaimer
        # -------------------------------------------------
    
        disclaimer = """
        <b>Disclaimer:</b> This assessment is based on publicly accessible
        information at the time of analysis. Results may change as website
        infrastructure or configurations are updated.
        """
    
        story.append(
            Paragraph(
                disclaimer,
                self.small_style
            )
        )
    
        story.append(PageBreak())
    
