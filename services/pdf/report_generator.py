    # =====================================================
    # Health Breakdown
    # =====================================================
    
    def _add_health_breakdown(self, story):
        """
        Adds the detailed health score breakdown.
        """
    
        story.append(self._section_title("Infrastructure Health Breakdown"))
    
        health = self.report.get("health", {}).get("data", {})
    
        score = health.get("score", 0)
    
        breakdown = health.get("breakdown", {})
    
        intro = f"""
        The overall infrastructure health score for
        <b>{self.domain}</b> is
        <b>{score}/100</b>.
    
        The score is calculated by evaluating multiple
        infrastructure and security components.
        """
    
        story.append(
            Paragraph(
                intro,
                self.body_style
            )
        )
    
        story.append(Spacer(1, 0.25 * inch))
    
        if not breakdown:
    
            story.append(
                Paragraph(
                    "Health breakdown data is unavailable.",
                    self.body_style
                )
            )
    
            story.append(PageBreak())
    
            return
    
        rows = [["Component", "Score", "Status"]]


        for component, values in breakdown.items():

            component_score = values.get("score", 0)

            maximum = values.get("max", 100)

            percentage = round((component_score / maximum) * 100) if maximum else 0

            if percentage >= 90:
                status = "Excellent"

            elif percentage >= 75:
                status = "Good"

            elif percentage >= 60:
                status = "Fair"

            else:
                status = "Needs Attention"

            rows.append([

                component,

                f"{component_score}/{maximum}",

                status

            ])

        # ----------------------------------
        # Overall Total
        # ----------------------------------

        if score >= 90:
            overall_status = "Excellent"

        elif score >= 75:
            overall_status = "Good"

        elif score >= 60:
            overall_status = "Fair"

        else:
            overall_status = "Needs Attention"

        rows.append([
            "TOTAL",

            f"{score}/100",

            overall_status

        ])
    
        table = Table(
            rows,
            colWidths=[
                3.2 * inch,
                1.0 * inch,
                2.0 * inch
            ]
        )

        last_row = len(rows) - 1
    
        table.setStyle(
    
            TableStyle([

                ("BACKGROUND", (0, 0), (-1, 0), self.primary),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),

                ("BACKGROUND", (0, 1), (0, -2), self.light),

                ("GRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),

                ("ALIGN", (1, 1), (1, -1), "CENTER"),
                ("ALIGN", (2, 1), (2, -1), "CENTER"),

                ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),

                ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                ("TOPPADDING", (0, 0), (-1, -1), 8),

                ("FONTNAME", (0, 1), (0, -1), "Helvetica-Bold"),

            # ----------------------------------
            # TOTAL Row
            # ----------------------------------

                ("BACKGROUND", (0, last_row), (-1, last_row), self.light),

                ("FONTNAME", (0, last_row), (-1, last_row), "Helvetica-Bold"),

                ("LINEABOVE", (0, last_row), (-1, last_row), 1.5, self.primary),

            ])
    
        )
    
        story.append(table)
    
        story.append(Spacer(1, 0.35 * inch))
    
        story.append(
            Paragraph(
                "<b>Score Interpretation</b>",
                self.subheading_style
            )
        )
    
        legend = Table(
            [
                ["90–100", "Excellent"],
                ["75–89", "Good"],
                ["60–74", "Fair"],
                ["0–59", "Needs Attention"],
            ],
            colWidths=[1.2 * inch, 2.5 * inch]
        )
    
        legend.setStyle(
    
            TableStyle([
    
                ("BACKGROUND", (0, 0), (0, -1), self.light),
    
                ("GRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
    
                ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
    
                ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                ("TOPPADDING", (0, 0), (-1, -1), 8),
    
            ])
    
        )
    
        story.append(legend)
    
        story.append(PageBreak())
    
    # =====================================================
    # DNS Analysis
    # =====================================================
    
    def _add_dns_section(self, story):
        """
        Adds the DNS Analysis section.
        """
    
        story.append(self._section_title("DNS Analysis"))
    
        dns = self.report.get("dns", {})
    
        if not dns.get("success"):
    
            story.append(
                Paragraph(
                    "DNS analysis could not be completed.",
                    self.body_style,
                )
            )
    
            story.append(PageBreak())
            return
    
        records = dns.get("data", {})
    
        total_types = len(records)
    
        total_records = sum(
            len(v) if isinstance(v, list) else 1
            for v in records.values()
        )
    
        overview = f"""
        DNS (Domain Name System) records determine how a domain is
        resolved on the Internet. NetInsight discovered
        <b>{total_records}</b> records across
        <b>{total_types}</b> record types.
        """
    
        story.append(
            Paragraph(
                overview,
                self.body_style,
            )
        )
    
        story.append(Spacer(1, 0.20 * inch))
    
        # -------------------------------------------------
        # Key Findings
        # -------------------------------------------------
    
        story.append(
            Paragraph(
                "<b>Key Findings</b>",
                self.subheading_style,
            )
        )
    
        findings = []
    
        if records.get("A"):
            findings.append("IPv4 (A) records are configured.")
    
        if records.get("AAAA"):
            findings.append("IPv6 (AAAA) records are available.")
    
        if records.get("MX"):
            findings.append("Mail Exchange (MX) records are configured.")
    
        if records.get("TXT"):
            findings.append("TXT records detected.")
    
        if records.get("NS"):
            findings.append("Authoritative Name Servers are configured.")
    
        if records.get("CAA"):
            findings.append("CAA records restrict certificate issuance.")
    
        if not findings:
            findings.append("No significant DNS records were detected.")
    
        story.append(self._bullet_list(findings))
    
        story.append(Spacer(1, 0.25 * inch))
    
        # -------------------------------------------------
        # DNS Summary
        # -------------------------------------------------
    
        story.append(
            Paragraph(
                "<b>DNS Record Summary</b>",
                self.subheading_style,
            )
        )
    
        summary_rows = [
            ["Record Type", "Count"]
        ]
    
        for record_type, values in sorted(records.items()):
    
            if isinstance(values, list):
                count = len(values)
            else:
                count = 1
    
            summary_rows.append([
                record_type,
                str(count)
            ])
    
        summary_table = Table(
            summary_rows,
            colWidths=[
                2.5 * inch,
                1.2 * inch,
            ],
        )
    
        summary_table.setStyle(
            TableStyle([
    
                ("BACKGROUND", (0, 0), (-1, 0), self.primary),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
    
                ("BACKGROUND", (0, 1), (0, -1), self.light),
    
                ("GRID", (0, 0), (-1, -1), 0.25, colors.lightgrey),
    
                ("ALIGN", (1, 1), (1, -1), "CENTER"),
    
                ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                ("TOPPADDING", (0, 0), (-1, -1), 8),
    
            ])
        )
    
        story.append(summary_table)
    
        story.append(Spacer(1, 0.30 * inch))
    
        # -------------------------------------------------
        # DNS Record Details
        # -------------------------------------------------
    
        story.append(
            Paragraph(
                "<b>DNS Record Details</b>",
                self.subheading_style,
            )
        )
    
        detail_rows = [["Record Type", "Value"]]
    
        preferred_order = [
            "A",
            "AAAA",
            "CNAME",
            "MX",
            "TXT",
            "NS",
            "CAA",
            "SOA",
            "SRV",
            "PTR",
        ]
    
        processed = set()
    
        for record_type in preferred_order:
    
            if record_type not in records:
                continue
    
            processed.add(record_type)
    
            values = records.get(record_type)
    
            if not isinstance(values, list):
                values = [values]
    
            if not values:
                detail_rows.append([
                    record_type,
                    "Not Configured",
                ])
                continue
    
            for value in values:
    
                if value is None:
                    display_value = "-"
    
                elif isinstance(value, dict):
    
                    if record_type == "MX":
                        priority = value.get("priority", "")
                        exchange = value.get("exchange", "")
                        display_value = f"Priority {priority} → {exchange}"
    
                    elif record_type == "SOA":
                        primary = value.get("primary_ns", "")
                        admin = value.get("admin_email", "")
                        display_value = f"{primary} ({admin})"
    
                    else:
                        display_value = ", ".join(
                            f"{k}: {v}"
                            for k, v in value.items()
                        )
    
                else:
                    display_value = str(value)
    
